package com.chinmaybiswaltec;

public class ExponentsInDoublePrimitive {
    public static void main(String[] args) {
        double price = 3.2E+4;
        // 3.2*10^4=32000
        System.out.println("price value "+price);
        double exp = 3.2E-4;
        // 3.2*10^-4
        System.out.println("exp value "+exp);
    }
}
//output:: price value 32000.0
//exp value 3.2E-4